package dom.funda;

public class PrimitiveTypes {

	public static void main(String[] args) {
		PrivitiveDataTypes privitiveDataTypes = new PrivitiveDataTypes();
		System.out.println(privitiveDataTypes.bVar);
		System.out.println(privitiveDataTypes.intVar);
		System.out.println(privitiveDataTypes.charVar);
		System.out.println(privitiveDataTypes.floatVar);
		System.out.println(privitiveDataTypes.doublVar);
		System.out.println(privitiveDataTypes.shortVar);
		System.out.println(privitiveDataTypes.longVar);
		System.out.println(privitiveDataTypes.booleanVar);
	}
}
class PrivitiveDataTypes {
	byte bVar;
	boolean booleanVar;
	int intVar;
	float floatVar;
	double doublVar;
	long longVar;
	short shortVar;
	char charVar;

}